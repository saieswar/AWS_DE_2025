def add_numbers(x: int, y: int) -> int:
    """This function takes two integers and returns their sum"""
    return x + y

def subtract_numbers(x: int, y: int) -> int:
    """This function takes two integers and returns their difference"""
    return x - y

def divide_numbers(x: int, y: int) -> float:
    """This function takes two integers and returns their division"""
    return x / y

def multiply_numbers(x: int, y: int) -> int:
    """This function takes two integers and returns their multiplication"""
    return x * y